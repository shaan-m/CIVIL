# Example array of lists
array_of_lists = [[1, 2, 3], [4, 5, 6], [7, 8, 9]]

# Accessing elements
element_1 = array_of_lists[0]     # Access the first list [1, 2, 3]
element_2 = array_of_lists[1]     # Access the second list [4, 5, 6]
element_3 = array_of_lists[2]     # Access the third list [7, 8, 9]

# Accessing specific elements within a list
value_1 = array_of_lists[0][0]    # Access the first element of the first list: 1
value_2 = array_of_lists[1][2]    # Access the third element of the second list: 6
value_3 = array_of_lists[2][1]    # Access the second element of the third list: 8

# Printing the accessed elements
print(element_1)
print(element_2)
print(element_3)

print(value_1)
print(value_2)
print(value_3)
